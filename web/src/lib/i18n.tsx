const LOCALE_LABELS: Record<Locale, string> = {
  zh: '中文',
  en: 'English',
  ja: '日本語',
  ko: '한국어',
  es: 'Español',
  fr: 'Français',
  de: 'Deutsch',
}

const locales: Record<Locale, Record<string, string>> = { zh, en, ja, ko, es, fr, de }

interface I18nContextType {
  locale: Locale
  setLocale: (l: Locale) => void
  t: (key: string) => string
}

const I18nContext = createContext<I18nContextType>({
  locale: 'zh',
  setLocale: () => {},
  t: (key) => key,
})

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => {
    const saved = typeof localStorage !== 'undefined' ? localStorage.getItem('clawclip-lang') : null
    if (saved && saved in locales) return saved as Locale
    return 'zh'
  })

  const setLocale = useCallback((l: Locale) => {
    setLocaleState(l)
    localStorage.setItem('clawclip-lang', l)
  }, [])

  const t = useCallback((key: string) => {
    return locales[locale]?.[key] ?? locales.en[key] ?? locales.zh[key] ?? key
  }, [locale])

  const value = useMemo(() => ({ locale, setLocale, t }), [locale, setLocale, t])

  return (
    <I18nContext.Provider value={value}>
      {children}
    </I18nContext.Provider>
  )
}

export function useI18n() {
  return useContext(I18nContext)
}

export function LanguageSwitcher() {
  const { locale, setLocale } = useI18n()
  return (
    <select
      value={locale}
      onChange={(e) => setLocale(e.target.value as Locale)}
      className="text-[11px] text-slate-400 bg-transparent border border-white/[0.08] rounded-lg px-2 py-1 outline-none cursor-pointer hover:border-white/[0.15] transition-colors"
    >
      {(Object.entries(LOCALE_LABELS) as [Locale, string][]).map(([code, label]) => (
        <option key={code} value={code} className="bg-[#0b1120] text-slate-300">
          {label}
        </option>
      ))}
    </select>
  )
}
